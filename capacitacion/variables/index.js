// Variables

// las variables en javascript pueden declararse como:
// var o let: su contenido puede ser modificado
// const: es constante y su contenido no puede modificarse en el transcurso del programa

var nombre = "Jose";
var apellido = "Gomez";

console.log(nombre, apellido);

nombre = "Miguel";
console.log(nombre, apellido);

const direccion = "San Martin 1234";
console.log(direccion);
